package com.example.danielco56.stayfocused;

public class Alcool {
    int cantitate;
    int concentratie;

    public Alcool(int cantitate, int concentratie) {
        this.cantitate = cantitate;
        this.concentratie = concentratie;
    }

    public int getCantitate() {
        return cantitate;
    }

    public int getConcentratie() {
        return concentratie;
    }
}
